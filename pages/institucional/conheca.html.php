	<h2>Fundação Unicesumar</h2>

	<p>A Fundação Cesumar foi criada no ano de 1999 com o objetivo de operar uma emissora de rádio e um canal de televisão, ambos de caráter educativo e sem finalidade comercial, destinados ao estágio e aperfeiçoamento dos acadêmicos dos cursos de Comunicação Social – Jornalismo e Publicidade e Propaganda - da Unicesumar.

	</p><p>É sua finalidade promover a educação, a cultura e as artes, por meio da divulgação dos eventos da instituição Unicesumar assim como das comunidades locais, regionais e universais que traduzam manifestações culturais, visando ao enriquecimento cultural dos telespectadores e ouvintes.

	</p><p>Na consecução de seus objetivos, a Fundação Cesumar pode ainda permitir que estabelecimentos de ensino do município e de municípios limitados pelo alcance da emissora participem da programação, mediante convênio e/ou acordo firmado entre as partes.
	</p>
